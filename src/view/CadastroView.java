        boolean status;
        int resposta;

        ProdutosDTO produto= new ProdutosDTO ();
        ProdutosDAO dao = new ProdutosDAO();
        
        produto.setNome(txtNome.getText());
        produto.setValor(Integer.parseInt(txtValor.getText()));
        produto.setStatus(txtStatus.getText());
